// deBAMLHelperTemplate is the callback-based native ctx.output_format
// pre-substitution helper, emitted NEXT TO the generated adapter.go
// whenever the de-BAML dynamic method is wired (Options.DeBAMLDynamicMethod
// set and a BuildRequest closure consumes it). Emitting it from codegen —
// rather than relying on a hand-written file in one package — is what lets
// BOTH the dynclient module AND the production server-side generated
// package get the helper, so BAML_REST_USE_DEBAML is live on the REST
// production path and not just for direct dynclient callers (#536).
//
// The helper depends only on public types: bamlutils (the Adapter,
// DeBAMLConfig, DeBAMLRenderFunc / DeBAMLParseFunc seam) and the per-build
// BAML types package (Baml_Rest_Message / Baml_Rest_ContentPart /
// Baml_Rest_DynamicOutput, fixed by cmd/build/dynamic.baml). The render and
// parse callbacks are injected by the root module — the generated package
// never imports internal/schema + outputformat, so it stays buildable as a
// standalone module.
//
// It also carries the native response-parser seam (maybeParseDeBAMLFinal +
// the JSON->envelope wrapper). The parser helpers are always emitted with
// the renderer helper; the parse CALLS are spliced into the dynamic
// method's parseFinalFn / parse-only function by codegen_buildrequest.go /
// codegen_methods.go. An emitted-but-uncalled helper is a harmless unused
// package function.
//
// {{ }} delimiters do not appear in the body, so text/template is safe:
// every single-brace token in the body (struct literals, the
// "{output_format}" string) is passed through verbatim.
const deBAMLHelperTemplate = `// Code generated by codegen; DO NOT EDIT.

package {{.Pkg}}

import (
	"errors"
	"fmt"
	"strings"

	bamlutils "{{.InterfacesPkg}}"
	types "{{.TypesPkg}}"
)

// deBAMLRendererGetter is the narrow optional interface the adapter
// implements to expose the native ctx.output_format render callback. The
// root module wires the callback (it owns internal/schema + outputformat);
// the generated package only ever calls it through this public-typed seam,
// so it never imports those root internal packages and stays buildable as
// a standalone module.
type deBAMLRendererGetter interface {
	DeBAMLRenderer() bamlutils.DeBAMLRenderFunc
}

// outputFormatPlaceholder is the literal string placeholder the dynamic
// BAML template replaces with ctx.output_format inside plain-string
// message content (cmd/build/dynamic.baml). The native path replaces the
// same token so string-content messages match BAML's behaviour.
const outputFormatPlaceholder = "{output_format}"

// maybeApplyDeBAMLOutputFormat returns the message slice the dynamic
// BuildRequest closures should hand to BAML's Request/StreamRequest. When
// the de-BAML flag is on and the carried schema renders, it returns a
// COPY of msgs with the output-format markers replaced by the natively-
// rendered block; otherwise it returns msgs unchanged.
//
// It NEVER mutates the input slice or its backing parts/content. The
// caller keeps the original slice for the legacy fallback children
// (legacyStreamChildFn / legacyCallChildFn), which must stay BAML-as-today
// — the native seam lives on the BuildRequest route only (route coupling
// tracked in #537). Returning the input unchanged when nothing is
// rewritten avoids any allocation on the flag-off / no-schema / nil-
// callback / render-error paths.
func maybeApplyDeBAMLOutputFormat(adapter bamlutils.Adapter, msgs []types.Baml_Rest_Message) []types.Baml_Rest_Message {
	if !adapter.DeBAMLConfig().Enabled {
		return msgs
	}
	outputSchema := adapter.DeBAMLOutputSchema()
	if outputSchema == nil {
		return msgs
	}
	getter, ok := adapter.(deBAMLRendererGetter)
	if !ok {
		return msgs
	}
	render := getter.DeBAMLRenderer()
	if render == nil {
		return msgs
	}
	block, err := render(outputSchema)
	if err != nil {
		logDeBAMLFallback(adapter, "render native output_format", err)
		return msgs
	}
	return rewriteOutputFormat(msgs, block)
}

// rewriteOutputFormat returns a copy of msgs with every output-format
// marker replaced by block: a structured output_format content part
// becomes a plain text part carrying the block, and a literal
// "{output_format}" token in string content is replaced. The input slice
// and its backing parts/content are never mutated — only-touched messages
// get a fresh parts slice / content pointer in the returned copy.
func rewriteOutputFormat(msgs []types.Baml_Rest_Message, block string) []types.Baml_Rest_Message {
	out := make([]types.Baml_Rest_Message, len(msgs))
	copy(out, msgs)
	for i := range out {
		m := &out[i]
		switch {
		case m.Parts != nil:
			if !hasOutputFormatPart(*m.Parts) {
				continue
			}
			newParts := make([]types.Baml_Rest_ContentPart, len(*m.Parts))
			copy(newParts, *m.Parts)
			for j := range newParts {
				p := &newParts[j]
				if p.Output_format != nil && *p.Output_format {
					text := block
					p.Text = &text
					p.Output_format = nil
				}
			}
			m.Parts = &newParts
		case m.Content != nil:
			if strings.Contains(*m.Content, outputFormatPlaceholder) {
				replaced := strings.ReplaceAll(*m.Content, outputFormatPlaceholder, block)
				m.Content = &replaced
			}
		}
	}
	return out
}

// hasOutputFormatPart reports whether any part is an output_format marker.
func hasOutputFormatPart(parts []types.Baml_Rest_ContentPart) bool {
	for i := range parts {
		if parts[i].Output_format != nil && *parts[i].Output_format {
			return true
		}
	}
	return false
}

// logDeBAMLFallback records a native-render fallback so the BAML-as-today
// path is observable without a user-facing flag. Best-effort: no logger
// installed means no line, never an error to the caller.
func logDeBAMLFallback(adapter bamlutils.Adapter, stage string, err error) {
	if logger := adapter.Logger(); logger != nil {
		logger.Warn("de-BAML output_format fallback to BAML render", "stage", stage, "err", err.Error())
	}
}

// deBAMLParserGetter is the narrow optional interface the adapter
// implements to expose the native response-parser callback. Like
// deBAMLRendererGetter it keeps the generated package free of any
// internal/schema dependency: the root module wires the parser in and the
// generated code drives it only through this public-typed seam.
type deBAMLParserGetter interface {
	DeBAMLParser() bamlutils.DeBAMLParseFunc
}

// maybeParseDeBAMLFinal attempts the native de-BAML final parse for the
// dynamic method, returning the generated dynamic-output envelope on
// success. The boolean reports whether the native parser CLAIMED a result:
//
//   - (envelope, true, nil): native produced the result; use it.
//   - (zero, false, nil): native declined — de-BAML off, no carried
//     schema, no parser wired, or bamlutils.ErrDeBAMLParseUnsupported — so
//     the caller falls back to BAML for this final parse (logged once).
//   - (zero, false, err): native CLAIMED a parse failure; the caller
//     propagates it so the native-vs-BAML differential catches drift
//     rather than masking it behind a silent fallback.
//
// The adapter doubles as the context.Context handed to the parser, mirroring
// how the generated Parse call passes the adapter as ctx.
func maybeParseDeBAMLFinal(adapter bamlutils.Adapter, raw string, stage string) (types.Baml_Rest_DynamicOutput, bool, error) {
	var zero types.Baml_Rest_DynamicOutput
	if !adapter.DeBAMLConfig().Enabled {
		return zero, false, nil
	}
	outputSchema := adapter.DeBAMLOutputSchema()
	if outputSchema == nil {
		return zero, false, nil
	}
	getter, ok := adapter.(deBAMLParserGetter)
	if !ok {
		return zero, false, nil
	}
	parse := getter.DeBAMLParser()
	if parse == nil {
		return zero, false, nil
	}
	res, err := parse(adapter, bamlutils.DeBAMLParseRequest{Raw: raw, OutputSchema: outputSchema})
	if err != nil {
		if errors.Is(err, bamlutils.ErrDeBAMLParseUnsupported) {
			logDeBAMLParseFallback(adapter, stage, err)
			return zero, false, nil
		}
		return zero, false, err
	}
	out, werr := wrapDeBAMLDynamicOutput(res.JSON)
	if werr != nil {
		// The native parser CLAIMED a success (nil error) but produced JSON
		// we cannot wrap into the dynamic-output envelope (malformed,
		// non-object, or scalar). Per the seam contract only
		// ErrDeBAMLParseUnsupported falls back to BAML; every other failure
		// on a claimed result propagates, so a parser/callback bug surfaces
		// instead of being hidden behind a BAML parse.
		return zero, false, fmt.Errorf("wrap native de-BAML parse result: %w", werr)
	}
	return out, true, nil
}

// wrapDeBAMLDynamicOutput wraps the flattened native parse JSON into the
// generated dynamic-output envelope so the existing newResultFn /
// FlattenDynamicOutput / InjectAbsentOptionals / ordering pipeline runs
// unchanged. DynamicProperties decodes in wire order; the downstream
// reorder/sort pass remains the order authority.
func wrapDeBAMLDynamicOutput(flat []byte) (types.Baml_Rest_DynamicOutput, error) {
	var out types.Baml_Rest_DynamicOutput
	if err := out.DynamicProperties.UnmarshalJSON(flat); err != nil {
		return types.Baml_Rest_DynamicOutput{}, err
	}
	return out, nil
}

// logDeBAMLParseFallback records a native-parse fallback so the BAML-parse
// path stays observable without a user-facing flag. Best-effort: no logger
// installed means no line, never an error to the caller.
func logDeBAMLParseFallback(adapter bamlutils.Adapter, stage string, err error) {
	if logger := adapter.Logger(); logger != nil {
		logger.Warn("de-BAML parse fallback to BAML parse", "stage", stage, "err", err.Error())
	}
}
`

// deBAMLHelperData parameterizes deBAMLHelperTemplate. Pkg is the
// generated package name; InterfacesPkg and TypesPkg are the bamlutils
// and per-build BAML types import paths.
type deBAMLHelperData struct {
	Pkg           string
	InterfacesPkg string
	TypesPkg      string
}

// deBAMLHelperPath returns the path the helper is written to: debaml.go
// next to the generated adapter.go.
func deBAMLHelperPath(adapterOutputPath string) string {
	return filepath.Join(filepath.Dir(adapterOutputPath), "debaml.go")
}

// maybeWriteDeBAMLHelper emits debaml.go next to adapter.go when the
// de-BAML call was actually emitted into a BuildRequest closure (set via
// g.emittedDeBAMLCall). It is a no-op otherwise, so generic / customer
// adapters that don't wire the de-BAML method carry no helper.
func (g *generator) maybeWriteDeBAMLHelper() {
	helperPath := deBAMLHelperPath(g.pkgs.OutputPath)
	if !g.emittedDeBAMLCall {
		// No de-BAML call was emitted, so remove any helper a previous
		// generation left in this directory. Without this, disabling
		// de-BAML (unsetting DeBAMLDynamicMethod, dropping the dynamic
		// method) or reusing an output dir across adapter versions would
		// leave an orphaned debaml.go behind. os.Remove is a no-op when
		// the file is absent (the common case for every non-de-BAML
		// generator).
		if err := os.Remove(helperPath); err != nil && !os.IsNotExist(err) {
			panic(fmt.Sprintf("codegen: remove stale de-BAML helper: %v", err))
		}
		return
	}
	tmpl, err := template.New("debaml").Parse(deBAMLHelperTemplate)
	if err != nil {
		panic(fmt.Sprintf("codegen: parse de-BAML helper template: %v", err))
	}
	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, deBAMLHelperData{
		Pkg:           g.pkgs.OutputPkgName,
		InterfacesPkg: g.pkgs.InterfacesPkg,
		TypesPkg:      g.pkgs.GeneratedClientPkg + "/types",
	}); err != nil {
		panic(fmt.Sprintf("codegen: execute de-BAML helper template: %v", err))
	}
	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		panic(fmt.Sprintf("codegen: gofmt de-BAML helper: %v\n%s", err, buf.String()))
	}
	if err := os.WriteFile(helperPath, formatted, 0o644); err != nil {
		panic(fmt.Sprintf("codegen: write de-BAML helper: %v", err))
	}
}
