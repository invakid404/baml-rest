// Generated table shapes (mirror the runtime types in tables.go).
type lowerEntry struct{ from, to rune }
type lowerFullEntry struct {
	from rune
	seq  []rune
}
type cccEntryGen struct {
	lo, hi rune
	class  uint8
}
type nfkdIdxGen struct{ cp, off, length int32 }

// tables is the full set of in-memory generated data.
type tables struct {
	lowerSingle []lowerEntry
	lowerFull   []lowerFullEntry

	alphabetic17    []urange
	numberGC17      []urange
	cased17         []urange
	caseIgnorable17 []urange
	whiteSpace17    []urange

	ccc16     []cccEntryGen
	nfkdIndex []nfkdIdxGen
	nfkdPool  []rune
	combining []urange
}

// buildLower derives the Rust to_lower table split into 1->1 and 1->many parts.
func buildLower(u *unicodeData, sc *specialLower) (single []lowerEntry, full []lowerFullEntry) {
	seen := map[rune]bool{}
	var cps []rune
	for cp, rec := range u.records {
		if rec.hasLC {
			cps = append(cps, cp)
			seen[cp] = true
		}
	}
	for cp := range sc.full {
		if !seen[cp] {
			cps = append(cps, cp)
		}
	}
	sort.Slice(cps, func(i, j int) bool { return cps[i] < cps[j] })
	for _, cp := range cps {
		var eff []rune
		if f, ok := sc.full[cp]; ok {
			eff = f
		} else if rec := u.records[cp]; rec != nil && rec.hasLC {
			eff = []rune{rec.simpleLC}
		}
		switch {
		case len(eff) == 0:
			continue
		case len(eff) == 1:
			if eff[0] == cp {
				continue // identity
			}
			single = append(single, lowerEntry{cp, eff[0]})
		default:
			full = append(full, lowerFullEntry{cp, append([]rune(nil), eff...)})
		}
	}
	return single, full
}

// buildCCC coalesces nonzero canonical combining classes into ranges.
func buildCCC(u *unicodeData) []cccEntryGen {
	var cps []rune
	for cp, rec := range u.records {
		if rec.ccc != 0 {
			cps = append(cps, cp)
		}
	}
	sort.Slice(cps, func(i, j int) bool { return cps[i] < cps[j] })
	var out []cccEntryGen
	for _, cp := range cps {
		cls := u.records[cp].ccc
		if n := len(out); n > 0 && out[n-1].class == cls && out[n-1].hi+1 == cp {
			out[n-1].hi = cp
		} else {
			out = append(out, cccEntryGen{cp, cp, cls})
		}
	}
	return out
}

// buildNFKD computes the fully-recursive compatibility decomposition table.
func buildNFKD(u *unicodeData) (index []nfkdIdxGen, pool []rune) {
	memo := map[rune][]rune{}
	var expand func(r rune) []rune
	expand = func(r rune) []rune {
		if v, ok := memo[r]; ok {
			return v
		}
		rec := u.records[r]
		if rec == nil || len(rec.decomp) == 0 {
			res := []rune{r}
			memo[r] = res
			return res
		}
		var res []rune
		for _, e := range rec.decomp {
			res = append(res, expand(e)...)
		}
		memo[r] = res
		return res
	}

	var cps []rune
	for cp, rec := range u.records {
		if len(rec.decomp) > 0 {
			cps = append(cps, cp)
		}
	}
	sort.Slice(cps, func(i, j int) bool { return cps[i] < cps[j] })
	for _, cp := range cps {
		seq := expand(cp)
		index = append(index, nfkdIdxGen{int32(cp), int32(len(pool)), int32(len(seq))})
		pool = append(pool, seq...)
	}
	return index, pool
}

// ---- Go source emission --------------------------------------------------

func fileHeader(source string) string {
	var b strings.Builder
	b.WriteString("// Code generated by internal/debaml/bamlunicode/cmd/gen; DO NOT EDIT.\n")
	b.WriteString("//\n")
	fmt.Fprintf(&b, "// BAML compatibility profile %s\n", genBAMLVersion)
	fmt.Fprintf(&b, "//   case + properties : Rust std Unicode %s (rustc %s)\n", genRustUnicode, genRustcVersion)
	fmt.Fprintf(&b, "//   normalization     : %s %s -> Unicode %s\n", genNormCrate, genNormVersion, genNormUnicode)
	fmt.Fprintf(&b, "// generator-revision  : %s\n", genRevision)
	fmt.Fprintf(&b, "// source              : %s\n", source)
	b.WriteString("\npackage bamlunicode\n\n")
	return b.String()
}

func emitRanges(b *strings.Builder, name string, rs []urange) {
	fmt.Fprintf(b, "var %s = []urange{\n", name)
	for i, r := range rs {
		if i%6 == 0 {
			b.WriteByte('\t')
		}
		fmt.Fprintf(b, "{0x%04X, 0x%04X},", r.lo, r.hi)
		if i%6 == 5 {
			b.WriteByte('\n')
		} else {
			b.WriteByte(' ')
		}
	}
	if len(rs)%6 != 0 {
		b.WriteByte('\n')
	}
	b.WriteString("}\n\n")
}

func emitLowerFile(t *tables, source string) ([]byte, error) {
	var b strings.Builder
	b.WriteString(fileHeader(source))
	fmt.Fprintf(&b, "// lowerSingle holds the %d one-to-one lowercase mappings (sorted by from).\n", len(t.lowerSingle))
	b.WriteString("var lowerSingle = []lowerEntry{\n")
	for i, e := range t.lowerSingle {
		if i%4 == 0 {
			b.WriteByte('\t')
		}
		fmt.Fprintf(&b, "{0x%04X, 0x%04X},", e.from, e.to)
		if i%4 == 3 {
			b.WriteByte('\n')
		} else {
			b.WriteByte(' ')
		}
	}
	if len(t.lowerSingle)%4 != 0 {
		b.WriteByte('\n')
	}
	b.WriteString("}\n\n")

	fmt.Fprintf(&b, "// lowerFull holds the %d unconditional one-to-many lowercase mappings.\n", len(t.lowerFull))
	b.WriteString("var lowerFull = []lowerFullEntry{\n")
	for _, e := range t.lowerFull {
		fmt.Fprintf(&b, "\t{0x%04X, []rune{%s}},\n", e.from, hexRunes(e.seq))
	}
	b.WriteString("}\n")
	return format.Source([]byte(b.String()))
}

func emitPropsFile(t *tables, source string) ([]byte, error) {
	var b strings.Builder
	b.WriteString(fileHeader(source))
	b.WriteString("// Unicode 17.0.0 (Rust std) character properties.\n\n")
	b.WriteString("// alphabetic17 is the DerivedCoreProperties Alphabetic property.\n")
	emitRanges(&b, "alphabetic17", t.alphabetic17)
	b.WriteString("// numberGC17 is General_Category = N (Nd, Nl, No).\n")
	emitRanges(&b, "numberGC17", t.numberGC17)
	b.WriteString("// cased17 is the DerivedCoreProperties Cased property (Final_Sigma input).\n")
	emitRanges(&b, "cased17", t.cased17)
	b.WriteString("// caseIgnorable17 is the DerivedCoreProperties Case_Ignorable property (Final_Sigma input).\n")
	emitRanges(&b, "caseIgnorable17", t.caseIgnorable17)
	b.WriteString("// whiteSpace17 is the PropList White_Space property (trim/is_whitespace).\n")
	emitRanges(&b, "whiteSpace17", t.whiteSpace17)
	return format.Source([]byte(b.String()))
}

func emitNFKDFile(t *tables, source string) ([]byte, error) {
	var b strings.Builder
	b.WriteString(fileHeader(source))
	b.WriteString("// Unicode 16.0.0 (unicode-normalization) decomposition data.\n\n")

	fmt.Fprintf(&b, "// ccc16 holds nonzero canonical combining classes as %d coalesced ranges.\n", len(t.ccc16))
	b.WriteString("var ccc16 = []cccEntry{\n")
	for i, e := range t.ccc16 {
		if i%3 == 0 {
			b.WriteByte('\t')
		}
		fmt.Fprintf(&b, "{0x%04X, 0x%04X, %d},", e.lo, e.hi, e.class)
		if i%3 == 2 {
			b.WriteByte('\n')
		} else {
			b.WriteByte(' ')
		}
	}
	if len(t.ccc16)%3 != 0 {
		b.WriteByte('\n')
	}
	b.WriteString("}\n\n")

	fmt.Fprintf(&b, "// nfkdIndex maps %d scalars to fully-expanded compatibility decompositions\n", len(t.nfkdIndex))
	b.WriteString("// stored in nfkdPool (sorted by cp).\n")
	b.WriteString("var nfkdIndex = []nfkdIdx{\n")
	for i, e := range t.nfkdIndex {
		if i%4 == 0 {
			b.WriteByte('\t')
		}
		fmt.Fprintf(&b, "{0x%04X, %d, %d},", e.cp, e.off, e.length)
		if i%4 == 3 {
			b.WriteByte('\n')
		} else {
			b.WriteByte(' ')
		}
	}
	if len(t.nfkdIndex)%4 != 0 {
		b.WriteByte('\n')
	}
	b.WriteString("}\n\n")

	fmt.Fprintf(&b, "// nfkdPool is the shared pool of %d decomposition runes.\n", len(t.nfkdPool))
	b.WriteString("var nfkdPool = []rune{\n")
	for i, r := range t.nfkdPool {
		if i%8 == 0 {
			b.WriteByte('\t')
		}
		fmt.Fprintf(&b, "0x%04X,", r)
		if i%8 == 7 {
			b.WriteByte('\n')
		} else {
			b.WriteByte(' ')
		}
	}
	if len(t.nfkdPool)%8 != 0 {
		b.WriteByte('\n')
	}
	b.WriteString("}\n")
	return format.Source([]byte(b.String()))
}

func emitMarksFile(t *tables, source string) ([]byte, error) {
	var b strings.Builder
	b.WriteString(fileHeader(source))
	b.WriteString("// Unicode 16.0.0 combining marks: General_Category Mn, Mc, Me\n")
	b.WriteString("// (unicode_normalization::char::is_combining_mark).\n")
	emitRanges(&b, "combiningMark16", t.combining)
	return format.Source([]byte(b.String()))
}

func hexRunes(rs []rune) string {
	parts := make([]string, len(rs))
	for i, r := range rs {
		parts[i] = fmt.Sprintf("0x%04X", r)
	}
	return strings.Join(parts, ", ")
}
