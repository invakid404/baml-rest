package bamlunicode

import (
	"bytes"
	"encoding/json"
	"os"
	"os/exec"
	"strings"
	"testing"
)

// TestNoHostUnicodeDeps enforces the core #555 invariant: the production runtime
// must consult ONLY the committed tables, never the host Go toolchain's Unicode
// data (unicode.*, golang.org/x/text) and never the network or build-target
// identity. It is part of CI drift-guard check B.
//
// The check is TRANSITIVE over first-party code: it walks the runtime package's
// full dependency closure via `go list -deps -json` and, for every FIRST-PARTY
// package in it, asserts none of its DIRECT (production) imports is a forbidden
// dependency. Scanning only this directory's direct imports would miss a
// production file importing a local helper that itself reaches host Unicode.
//
// Note: stdlib `strings` transitively imports `unicode`, which is benign — the
// invariant is that FIRST-PARTY code never DIRECTLY imports host Unicode data /
// network / build-target packages, so we recurse only through first-party edges.
func TestNoHostUnicodeDeps(t *testing.T) {
	capStress(t) // execs `go list`; deterministic — cap under -race -count=100

	const module = "github.com/invakid404/baml-rest"
	const runtimePkg = module + "/internal/debaml/bamlunicode"
	forbidden := []string{
		"unicode",           // Go stdlib Unicode tables (host version)
		"golang.org/x/text", // x/text Unicode tables (host version)
		"net",               // no runtime network
		"net/http",          // no runtime download
		"runtime",           // no GOOS/GOARCH/version branching
	}
	allowed := map[string]bool{"unicode/utf8": true} // pure UTF-8 codec, no data tables

	out, err := exec.Command("go", "list", "-deps", "-json", runtimePkg).Output()
	if err != nil {
		t.Fatalf("go list -deps -json %s: %v", runtimePkg, err)
	}
	dec := json.NewDecoder(bytes.NewReader(out))
	var scanned int
	for dec.More() {
		var p struct {
			ImportPath string
			Imports    []string
		}
		if err := dec.Decode(&p); err != nil {
			t.Fatalf("decode go list json: %v", err)
		}
		if p.ImportPath != module && !strings.HasPrefix(p.ImportPath, module+"/") {
			continue // stdlib / third-party: transitive host Unicode here is benign
		}
		scanned++
		for _, imp := range p.Imports {
			if allowed[imp] {
				continue
			}
			for _, bad := range forbidden {
				if imp == bad || strings.HasPrefix(imp, bad+"/") {
					t.Errorf("first-party package %s directly imports forbidden host-Unicode/network/target dependency %q", p.ImportPath, imp)
				}
			}
		}
	}
	if scanned == 0 {
		t.Fatal("scanned no first-party packages (go list returned nothing?)")
	}
	t.Logf("scanned %d first-party packages in the runtime closure; none directly depend on host Unicode/network/target", scanned)
}

// TestGeneratedHeaders asserts each committed generated table carries the
// provenance header (profile + generator revision), part of drift-guard check B.
func TestGeneratedHeaders(t *testing.T) {
	gens := []string{
		"lower_17_0_0_gen.go",
		"props_17_0_0_gen.go",
		"nfkd_16_0_0_gen.go",
		"marks_16_0_0_gen.go",
		"manifest_gen.go",
	}
	for _, name := range gens {
		data, err := os.ReadFile(name)
		if err != nil {
			t.Errorf("read %s: %v", name, err)
			continue
		}
		head := string(data)
		for _, want := range []string{
			"Code generated by internal/debaml/bamlunicode/cmd/gen; DO NOT EDIT.",
			"BAML compatibility profile " + BAMLVersion,
			"generator-revision",
		} {
			if !strings.Contains(head, want) {
				t.Errorf("%s: header missing %q", name, want)
			}
		}
		// Generated tables must not reference build-target identity.
		for _, bad := range []string{"GOOS", "GOARCH", "runtime.", "net/http"} {
			if strings.Contains(head, bad) {
				t.Errorf("%s: generated table references %q", name, bad)
			}
		}
	}
}
