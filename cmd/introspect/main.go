// extractBuildRequestMethods extracts methods from a *build_request or
// *build_request_stream receiver type. The methods have the same signature
// pattern as Stream methods: (ctx, args..., opts...) -> (baml.HTTPRequest, error)
func extractBuildRequestMethods(f *parsedFile, receiverName string) []map[string]any {
	var methods []map[string]any
	for _, decl := range f.file.Decls {
		funcDecl, ok := decl.(*ast.FuncDecl)
		if !ok || funcDecl.Recv == nil || len(funcDecl.Recv.List) == 0 {
			continue
		}
		receiver, ok := funcDecl.Recv.List[0].Type.(*ast.StarExpr)
		if !ok {
			continue
		}
		receiverIdent, ok := receiver.X.(*ast.Ident)
		if !ok || receiverIdent.Name != receiverName {
			continue
		}
		var args []string
		for _, param := range funcDecl.Type.Params.List {
			for _, name := range param.Names {
				args = append(args, name.Name)
			}
		}
		// Remove ctx (first) and opts (last variadic) to get just the function args
		if len(args) >= 2 {
			methods = append(methods, map[string]any{
				"name": funcDecl.Name.Name,
				"args": args[1 : len(args)-1],
			})
		}
	}
	return methods
}

// generateBuildRequestVars generates the Request/StreamRequest variables and
// their method/func maps. If requestFile or streamRequestFile is nil, generates
// nil/empty stubs (for BAML versions < 0.219.0).
//
// supportsWithClient feeds introspected.SupportsWithClient, used by the
// codegen.Generate(selfPkg) wrapper to pick the right Options without
// hardcoding a compile-time decision.
//
// retryPkg is the import path of the bamlutils retry subpackage, anchored to
// the configured InterfacesPkg so a non-default --interfaces-pkg propagates
// through to the forced import.
func generateBuildRequestVars(out *jen.File, requestFile, streamRequestFile *parsedFile, streamPkg string, supportsWithClient bool, retryPkg string) {
	// Force the import so the stub compiles even when the maps are empty
	out.Anon(retryPkg)

	if requestFile != nil {
		requestMethods := extractBuildRequestMethods(requestFile, "build_request")

		out.Comment("Request is the BAML Request singleton for building non-streaming HTTP requests")
		out.Var().Id("Request").Op("=").Qual(streamPkg, "Request")

		out.Comment("RequestFuncs maps Request method names to their function values (for reflection)")
		entries := make([]jen.Code, 0, len(requestMethods))
		for _, m := range requestMethods {
			name := m["name"].(string)
			entries = append(entries, jen.Lit(name).Op(":").Qual(streamPkg, "Request").Dot(name))
		}
		out.Var().Id("RequestFuncs").Op("=").Map(jen.String()).Any().Values(entries...)
	} else {
		out.Comment("Request is nil when BAML version < 0.219.0 (no BuildRequest API)")
		out.Var().Id("Request").Any()
		out.Var().Id("RequestFuncs").Op("=").Map(jen.String()).Any().Values()
	}

	if streamRequestFile != nil {
		streamRequestMethods := extractBuildRequestMethods(streamRequestFile, "build_request_stream")

		out.Comment("StreamRequest is the BAML StreamRequest singleton for building streaming HTTP requests")
		out.Var().Id("StreamRequest").Op("=").Qual(streamPkg, "StreamRequest")

		out.Comment("StreamRequestFuncs maps StreamRequest method names to their function values (for reflection)")
		entries := make([]jen.Code, 0, len(streamRequestMethods))
		for _, m := range streamRequestMethods {
			name := m["name"].(string)
			entries = append(entries, jen.Lit(name).Op(":").Qual(streamPkg, "StreamRequest").Dot(name))
		}
		out.Var().Id("StreamRequestFuncs").Op("=").Map(jen.String()).Any().Values(entries...)
	} else {
		out.Comment("StreamRequest is nil when BAML version < 0.219.0 (no BuildRequest API)")
		out.Var().Id("StreamRequest").Any()
		out.Var().Id("StreamRequestFuncs").Op("=").Map(jen.String()).Any().Values()
	}

	// SupportsWithClient — true when baml_client exposes the top-level
	// WithClient(string) CallOption (BAML v0.219.0+). Read by
	// codegen.Generate(selfPkg) so legacy callers without an explicit
	// Options struct get the right behaviour for the bundled BAML
	// runtime.
	out.Comment("SupportsWithClient is true when baml_client exposes WithClient(string) (BAML v0.219.0+)")
	out.Var().Id("SupportsWithClient").Op("=").Lit(supportsWithClient)
}

// bamlConfig holds parsed .baml configuration data.
type bamlConfig struct {
	// clientProvider maps client name → provider string
	clientProvider map[string]string
	// clientRetryPolicy maps client name → retry policy name
	clientRetryPolicy map[string]string
	// functionClient maps function name → client name
	functionClient map[string]string
	// retryPolicies maps policy name → {max_retries, strategy type, delay params}
	retryPolicies map[string]parsedRetryPolicy
	// fallbackChains maps strategy client name → ordered list of child client names.
	// The parser records any client whose options contain strategy
	// [ClientA, ClientB, ...]; provider filtering (for example restricting the
	// BuildRequest path to baml-fallback and leaving baml-roundrobin on legacy)
	// happens later at runtime in ResolveFallbackChain.
	fallbackChains map[string][]string
	// roundRobinStart maps baml-roundrobin client name → configured `start`
	// option value. Only populated for clients that set `start N` inside
	// their options block; absent entries leave the coordinator free to
	// pick a random seed. Provider filtering happens at runtime — the
	// parser records the value for any client whose options contain
	// `start N`, regardless of provider.
	roundRobinStart map[string]int
	// bedrockClientOptions maps client name → parsed aws-bedrock
	// options (endpoint_url, region, and the credential selectors
	// access_key_id / secret_access_key / session_token / profile).
	// The parser records entries for every client whose options block
	// declares any of those keys (so the map can carry literal-vs-env
	// provenance) regardless of provider; the emission step filters
	// to provider == "aws-bedrock" so a non-bedrock client that
	// happens to use the same option keys for an unrelated purpose
	// can't leak into the runtime BedrockClientOptions map.
	bedrockClientOptions map[string]bedrockClientOptions
	// validationErrors accumulates config-validation errors discovered
	// during walking. Generation fails (log.Fatalf at the start of
	// generateBamlConfigVars) if non-empty so that a misconfigured
	// project — for example an aws-bedrock client with a declared-empty
	// bedrock option like `region ""`, `access_key_id ""`, or
	// `secret_access_key ""` — cannot silently produce an introspected.go
	// that would later either fail at request-build time (region) or
	// silently fall through to the default credential chain and mask
	// the config bug (credentials). See bamlValueBedrockOption for the
	// per-key rationale and the corresponding upstream-BAML behaviours.
	validationErrors []bamlValidationError

	// staticSchemas holds the per-function static output-schema descriptor
	// built natively from the parsed .baml type system (de-BAML P3 slice 2,
	// #586), keyed by function name. Populated by buildStaticSchemas after all
	// files are parsed; a function appears here only when its WHOLE output
	// graph lowered + validated (schema.FromStaticDescriptor + ValidateOutput).
	// NOT consumed by codegen yet — this slice proves the builder; runtime
	// wiring is a later slice. See schemabuild.go.
	staticSchemas map[string]schemadescriptor.Bundle
	// staticSchemaDeclines maps a function name to the stable reason its output
	// graph could not be represented faithfully (recursion, attributes, media,
	// tuple, float literal, @@dynamic, @skip, ...). Fail-closed: a declined
	// function has NO entry in staticSchemas.
	staticSchemaDeclines map[string]string
}

// bamlValidationError captures a single semantic-validation failure
// surfaced during client-block walking. Field-scoped and client-scoped
// so the operator can locate the offending block at a glance.
type bamlValidationError struct {
	Client string
	Key    string
	Reason string
}

// Message formats the error for stderr / the combined generation error
// per the existing client-scoped error patterns elsewhere in introspect.
func (e bamlValidationError) Message() string {
	return fmt.Sprintf("aws-bedrock: invalid %s for client %q: %s", e.Key, e.Client, e.Reason)
}

// bedrockClientOptions captures the per-client aws-bedrock options that
// codegen needs at request-build time to override BAML's hardcoded
// modular Bedrock URL. BAML v0.219's HTTPRequest builder hardcodes
// https://bedrock-runtime.<region>.amazonaws.com and the public Go
// surface does not expose ClientDetails (see Codex's deep-dive Q1/Q2),
// so baml-rest carries its own resolved options keyed on client name.
type bedrockClientOptions struct {
	EndpointURL bedrockOptionValue
	Region      bedrockOptionValue
	Credentials bedrockCredentialOptions
}

// bedrockCredentialOptions groups the per-client aws-bedrock credential
// selectors (#254 item 2). Each field carries a literal-or-env
// bedrockOptionValue resolved at request-build time. Provenance is
// preserved so a generated literal stays a literal and an `env.X`
// reference resolves against the worker's current environment.
//
// Resolution precedence (applied by bamlutils/llmhttp.ResolveBedrockCredentials):
//  1. AccessKeyID + SecretAccessKey (+ optional SessionToken) → static.
//  2. Profile → shared-config profile load.
//  3. Default chain (none of the above declared).
//
// Security note: literal credential values declared inline (e.g.
// `secret_access_key "<example-key>"`) land in generated introspected.go
// AND the compiled worker binary — that is BAML-parity behavior,
// documented as an operator footgun. Use `env.X` to keep secret material
// out of generated artifacts.
type bedrockCredentialOptions struct {
	AccessKeyID     bedrockOptionValue
	SecretAccessKey bedrockOptionValue
	SessionToken    bedrockOptionValue
	Profile         bedrockOptionValue
}

// bedrockOptionValue stores a single .baml option value with its
// provenance preserved. Resolution to a final string happens at
// request-build time so an env-var override picks up the worker's
// current environment rather than the build-time snapshot.
//
// Provenance is the discriminator:
//   - ""          → no value declared
//   - "literal"   → Literal carries the bare string (quotes already stripped)
//   - "env"       → EnvVar carries the env var name (no leading "env.")
type bedrockOptionValue struct {
	Literal    string
	EnvVar     string
	Provenance string
}

type parsedRetryPolicy struct {
	maxRetries int
	strategy   string // "constant_delay" or "exponential_backoff"
	delayMs    int
	multiplier float64
	maxDelayMs int
}

// parseBamlSourceDir recursively reads all .baml files in the given directory
// tree and extracts client, function, and retry_policy blocks. Uses
// filepath.WalkDir so nested subdirectories (which real BAML projects use)
// are included.
func parseBamlSourceDir(dir string) *bamlConfig {
	cfg := &bamlConfig{
		clientProvider:       make(map[string]string),
		clientRetryPolicy:    make(map[string]string),
		functionClient:       make(map[string]string),
		retryPolicies:        make(map[string]parsedRetryPolicy),
		fallbackChains:       make(map[string][]string),
		roundRobinStart:      make(map[string]int),
		bedrockClientOptions: make(map[string]bedrockClientOptions),
	}

	// parsedFiles retains every successfully parsed file so the static-schema
	// builder can classify types across the whole project before lowering any
	// function's output graph (de-BAML P3 slice 2, #586). Config extraction
	// still runs per-file via processBAMLFile below; the schema build is an
	// additive second pass and does not change any config behaviour.
	var parsedFiles []nativeschema.SourceFile

	err := filepath.WalkDir(dir, func(path string, d os.DirEntry, err error) error {
		if err != nil {
			return nil // skip unreadable entries
		}
		if d.IsDir() || !strings.HasSuffix(d.Name(), ".baml") {
			return nil
		}
		data, readErr := os.ReadFile(path)
		if readErr != nil {
			return nil // skip unreadable files
		}
		file, parseErr := bamlparser.ParseBytes(path, data)
		if parseErr != nil {
			// The new parser is intentionally permissive (Codex scoping Q2/Q6);
			// any real-world parse error is a parser bug to be fixed by adding
			// a fixture rather than swallowing here. Match the old line walker's
			// silent-on-unreadable posture so generation stays best-effort.
			return nil
		}
		processBAMLFile(cfg, file)
		// Carry the raw source alongside the parsed File so the native builder
		// can detect `///` doc comments by declaration span (see #586 D6).
		parsedFiles = append(parsedFiles, nativeschema.SourceFile{File: file, Source: data})
		return nil
	})

	// Build the native per-function static schemas from the collected files.
	// This is fail-closed (per-function decline, never a pipeline error) and is
	// not yet consumed downstream, so it runs regardless of the walk outcome.
	cfg.staticSchemas, cfg.staticSchemaDeclines = nativeschema.BuildStaticSchemas(parsedFiles)

	if err != nil {
		// baml_src may not exist during stub generation
		return cfg
	}

	enrichShorthandClientProviders(cfg)
	return cfg
}

// enrichShorthandClientProviders walks every client name referenced by a
// function default or by a strategy chain and, for shorthand specs
// (`<provider>/<model>` per upstream BAML's ClientSpec::Shorthand) that
// have no entry in cfg.clientProvider yet, registers the parsed provider.
//
// Why this pass exists. BAML supports defaulting a function to a
// shorthand client without declaring a named `client<llm>` block —
// `function MyFunc { client "openai/gpt-4o" ... }`. The introspector
// records the literal "openai/gpt-4o" as the function's client name, but
// without this enrichment cfg.clientProvider has no entry for it, so the
// FunctionProvider materialization at generateBamlConfigVars never sets
// a provider, and the BuildRequest gate falls through to legacy via
// PathReasonEmptyProvider for every function defaulting to shorthand.
//
// The same lookup is performed for strategy-wrapper children
// (cfg.fallbackChains values) so a fallback or RR child written as a
// shorthand spec also lights up the BuildRequest path.
//
// Already-registered names take precedence; this pass only fills holes.
func enrichShorthandClientProviders(cfg *bamlConfig) {
	register := func(name string) {
		if name == "" {
			return
		}
		if _, exists := cfg.clientProvider[name]; exists {
			return
		}
		provider, _, ok := parseShorthandClient(name)
		if !ok {
			return
		}
		cfg.clientProvider[name] = provider
	}
	for _, clientName := range cfg.functionClient {
		register(clientName)
	}
	for _, chain := range cfg.fallbackChains {
		for _, child := range chain {
			register(child)
		}
	}
}

// canonicaliseProvider folds the round-robin and fallback strategy
// aliases that BAML upstream accepts onto the canonical baml-rest
// spellings so downstream maps — ClientProvider, router
// classification, metadata — only ever see one spelling per strategy:
//
//   - "baml-roundrobin" / "baml-round-robin" / "round-robin"
//     → "baml-roundrobin"
//   - "fallback" / "baml-fallback" → "baml-fallback"
//
// BAML upstream's clientspec.rs:139-142 accepts both forms for each
// strategy. The "fallback" alias is the spelling the BAML CLI init
// template emits (baml_cli_init.baml:118), so operators following the
// docs hit it routinely. Without this normalisation the BuildRequest
// classification would treat "fallback" as a single-provider client,
// fall to legacy via PathReasonUnsupportedProvider, and lose every
// BuildRequest-only capability for that client (chain plan, mixed-mode
// child handling, RR-child plumbing).
func canonicaliseProvider(provider string) string {
	switch provider {
	case "baml-roundrobin", "baml-round-robin", "round-robin":
		return "baml-roundrobin"
	case "baml-fallback", "fallback":
		return "baml-fallback"
	}
	return provider
}

// parseShorthandClient splits a BAML shorthand client spec ("<provider>/<model>")
// into its provider and model parts and reports ok=true when the leading
// segment is a provider string upstream's ClientSpec::new_from_id would
// accept. The returned provider is already run through canonicaliseProvider
// so callers see the same canonical spelling produced by parseClientBlock.
//
// Mirrors upstream BAML v0.219.0 clientspec.rs: ClientSpec::new_from_id
// splits on the first '/' and parses the left side through
// ClientProvider::from_str (clientspec.rs:28-36, :116-145). The allowlist
// here is the exact set FromStr accepts, including the `baml-*` legacy
// aliases — anything outside that set is treated as a non-shorthand
// (named) client by upstream, and the introspector must agree so
// FunctionProvider only gets populated for genuinely-shorthand defaults.
//
// Strategy providers (fallback, round-robin) are technically accepted by
// upstream's FromStr and therefore by ClientSpec::new_from_id; we include
// them for parser fidelity even though shorthand-as-strategy is
// degenerate (strategies need a children list). After canonicalisation
// they resolve to baml-fallback / baml-roundrobin and the downstream
// classifier routes them through the existing strategy paths.
func parseShorthandClient(spec string) (provider, model string, ok bool) {
	idx := strings.Index(spec, "/")
	if idx <= 0 || idx >= len(spec)-1 {
		return "", "", false
	}
	p := spec[:idx]
	m := spec[idx+1:]
	if !isKnownShorthandProvider(p) {
		return "", "", false
	}
	return canonicaliseProvider(p), m, true
}

// isKnownShorthandProvider reports whether p is a provider string that
// BAML upstream's ClientProvider::from_str would accept. Source list:
// clientspec.rs:119-144 (v0.219.0). Keep in lockstep with upstream — if
// a future BAML release adds a provider, this allowlist must follow.
func isKnownShorthandProvider(p string) bool {
	switch p {
	case "openai",
		"baml-openai-chat",
		"openai-generic",
		"azure-openai",
		"baml-azure-chat",
		"openai-responses",
		"ollama",
		"baml-ollama-chat",
		"openrouter",
		"anthropic",
		"baml-anthropic-chat",
		"aws-bedrock",
		"google-ai",
		"vertex-ai",
		"fallback",
		"baml-fallback",
		"round-robin",
		"baml-round-robin":
		return true
	}
	return false
}

// processBAMLFile dispatches the parsed AST's top-level items in source
// order, mirroring parseBamlFile's top-level top-down posture: only
// client (both `client<llm> Name { ... }` and plain `client Name { ... }`),
// function (with named signature), and retry_policy blocks contribute to
// *bamlConfig. Every other top-level shape — generators, classes, enums,
// type aliases, template_strings, tests, and clients with an unknown
// non-empty TypeParam — is intentionally ignored.
//
// Consume both `client<llm> Foo { ... }` and plain `client Foo { ... }`:
// upstream BAML's grammar accepts both spellings (CLIENT_KEYWORD =
// "client<llm>" | "client" in engine/baml-lib/ast/src/parser/datamodel.pest)
// and the parser-database walks them through the same visit_client path
// without distinguishing the source keyword (engine/baml-lib/parser-database
// /src/types/mod.rs visit_client). Our walker has no LLM-specific behavior
// gated on TypeParam after dispatch, so both spellings produce the same
// *bamlConfig shape. Refs #268. Future non-LLM type params (e.g. a
// hypothetical `client<openapi> Foo`) are intentionally NOT accepted here
// — we have no semantics for them yet.
func processBAMLFile(cfg *bamlConfig, f *bamlparser.File) {
	for _, it := range f.Items {
		switch {
		case it.Client != nil && (it.Client.TypeParam == "llm" || it.Client.TypeParam == "") && it.Client.Name != "":
			processBAMLClientBlock(cfg, it.Client)
		case it.Function != nil && it.Function.Name != "":
			processBAMLFunctionBlock(cfg, it.Function)
		case it.RetryPolicy != nil && it.RetryPolicy.Name != "":
			processBAMLRetryPolicyBlock(cfg, it.RetryPolicy)
		}
	}
}

// processBAMLClientBlock walks a parsed client block — either
// `client<llm> Name { ... }` or plain `client Name { ... }` — mirroring
// parseClientBlock's behaviour: stale-state cleanup for the client name,
// ordered top-level provider / retry_policy / options handling. Both
// spellings produce identical output; the source keyword is not encoded
// after dispatch (see processBAMLFile).
//
// Two-pass: first resolve the client's final provider (last-wins on
// duplicate `provider` fields), then walk fields in source order to
// apply side effects with correct provider-gated validation. The
// final-provider pre-pass is needed because options can appear before
// provider in the same block, and Bedrock-key validation
// (declared-empty `region`, `access_key_id`, `secret_access_key`)
// must only fire when the final provider is aws-bedrock.
func processBAMLClientBlock(cfg *bamlConfig, c *bamlparser.ClientBlock) {
	name := c.Name
	delete(cfg.roundRobinStart, name)
	delete(cfg.fallbackChains, name)
	delete(cfg.clientProvider, name)
	delete(cfg.clientRetryPolicy, name)
	delete(cfg.bedrockClientOptions, name)

	finalProvider := ""
	for _, f := range c.Fields {
		if f.Key == "provider" && f.Value != nil {
			finalProvider = canonicaliseProvider(bamlValueScalar(f.Value))
		}
	}
	isBedrockClient := finalProvider == "aws-bedrock"

	for _, f := range c.Fields {
		switch f.Key {
		case "provider":
			if f.Value != nil {
				cfg.clientProvider[name] = canonicaliseProvider(bamlValueScalar(f.Value))
			}
		case "retry_policy":
			if f.Value != nil {
				cfg.clientRetryPolicy[name] = bamlValueScalar(f.Value)
			}
		case "options":
			if f.Block != nil {
				processBAMLOptionsBlock(cfg, name, isBedrockClient, f.Block)
			}
		}
	}
}

// processBAMLOptionsBlock walks an options block. Strategy is captured
// at ANY depth (matching the old parser's depth-agnostic strategy
// extraction in extractStrategyStatement); start and Bedrock keys are
// honoured ONLY at the immediate options depth, matching
// extractRoundRobinStart's and updateBedrockClientOption's
// optionsDepth == 1 guard.
//
// isBedrockClient is the per-client gate computed by
// processBAMLClientBlock's first pass; it controls whether
// declared-empty Bedrock-key literals (currently: `region`,
// `access_key_id`, `secret_access_key`) are rejected as validation
// errors. A non-Bedrock client with the same option keys is recorded
// into bedrockClientOptions for parser fidelity (codegen filters by
// provider) and is NEVER subject to the empty-literal validation.
func processBAMLOptionsBlock(cfg *bamlConfig, name string, isBedrockClient bool, opts *bamlparser.Block) {
	for _, f := range opts.Fields {
		switch f.Key {
		case "strategy":
			if f.Value != nil && f.Value.List != nil {
				chain := bamlValueStrategyList(f.Value)
				if len(chain) > 0 {
					cfg.fallbackChains[name] = chain
				}
			}
		case "start":
			if f.Value != nil && f.Value.Number != nil {
				if n, err := strconv.ParseInt(*f.Value.Number, 10, 32); err == nil {
					cfg.roundRobinStart[name] = int(n)
				}
			}
		case "endpoint_url", "region",
			"access_key_id", "secret_access_key", "session_token", "profile":
			val, ok, verr := bamlValueBedrockOption(name, f.Key, isBedrockClient, f.Value)
			if verr != nil {
				cfg.validationErrors = append(cfg.validationErrors, *verr)
				continue
			}
			if ok {
				cur := cfg.bedrockClientOptions[name]
				switch f.Key {
				case "endpoint_url":
					cur.EndpointURL = val
				case "region":
					cur.Region = val
				case "access_key_id":
					cur.Credentials.AccessKeyID = val
				case "secret_access_key":
					cur.Credentials.SecretAccessKey = val
				case "session_token":
					cur.Credentials.SessionToken = val
				case "profile":
					cur.Credentials.Profile = val
				}
				cfg.bedrockClientOptions[name] = cur
			}
		}
		if f.Block != nil {
			processBAMLOptionsStrategyRecursive(cfg, name, f.Block)
		}
	}
}

// processBAMLOptionsStrategyRecursive walks block-valued option fields
// looking for nested `strategy [...]` lists. Only strategy is captured;
// `start` and Bedrock keys are gated to depth==1 by the caller.
func processBAMLOptionsStrategyRecursive(cfg *bamlConfig, name string, blk *bamlparser.Block) {
	for _, f := range blk.Fields {
		if f.Key == "strategy" && f.Value != nil && f.Value.List != nil {
			chain := bamlValueStrategyList(f.Value)
			if len(chain) > 0 {
				cfg.fallbackChains[name] = chain
			}
		}
		if f.Block != nil {
			processBAMLOptionsStrategyRecursive(cfg, name, f.Block)
		}
	}
}

// processBAMLFunctionBlock walks a function block, capturing every
// top-level `client VALUE` field. Last-wins on duplicates: the old
// parseFunctionBlock loop scans every line and overwrites
// functionClient[name] on each `client VALUE` match, so the walker
// iterates all matching fields rather than stopping at the first.
func processBAMLFunctionBlock(cfg *bamlConfig, fn *bamlparser.FunctionBlock) {
	for _, f := range fn.Fields {
		if f.Key != "client" || f.Value == nil {
			continue
		}
		cfg.functionClient[fn.Name] = bamlValueScalar(f.Value)
	}
}

// processBAMLRetryPolicyBlock walks a retry_policy block. Direct
// top-level fields (max_retries, type, delay_ms, multiplier,
// max_delay_ms) are applied first; nested `strategy { ... }` fields
// override them, mirroring the old expandStrategyBlock + allLines
// append precedence (strategy nested values win).
//
// Production-only behaviour preserved here: stderr warnings for invalid
// numeric values on max_retries / delay_ms / multiplier / max_delay_ms.
// The parity harness compares *bamlConfig and cannot observe stderr, so
// the warning emission is verified by inspection rather than by parity.
// The warning strings match parseRetryPolicyBlock byte-for-byte so
// anyone grep'ing logs for the old warning text still finds it.
func processBAMLRetryPolicyBlock(cfg *bamlConfig, r *bamlparser.RetryPolicyBlock) {
	p := parsedRetryPolicy{}
	apply := func(key string, v *bamlparser.Value) {
		switch key {
		case "max_retries":
			if v == nil {
				return
			}
			if n, err := strconv.Atoi(bamlValueScalar(v)); err != nil {
				fmt.Fprintf(os.Stderr, "warning: invalid max_retries value in retry_policy %s: %v\n", r.Name, err)
			} else {
				p.maxRetries = n
			}
		case "type":
			p.strategy = bamlValueScalar(v)
		case "delay_ms":
			if v == nil {
				return
			}
			if n, err := strconv.Atoi(bamlValueScalar(v)); err != nil {
				fmt.Fprintf(os.Stderr, "warning: invalid delay_ms value in retry_policy %s: %v\n", r.Name, err)
			} else {
				p.delayMs = n
			}
		case "multiplier":
			if v == nil {
				return
			}
			if fl, err := strconv.ParseFloat(bamlValueScalar(v), 64); err != nil {
				fmt.Fprintf(os.Stderr, "warning: invalid multiplier value in retry_policy %s: %v\n", r.Name, err)
			} else {
				p.multiplier = fl
			}
		case "max_delay_ms":
			if v == nil {
				return
			}
			if n, err := strconv.Atoi(bamlValueScalar(v)); err != nil {
				fmt.Fprintf(os.Stderr, "warning: invalid max_delay_ms value in retry_policy %s: %v\n", r.Name, err)
			} else {
				p.maxDelayMs = n
			}
		}
	}
	// Pass 1: direct top-level fields (excluding nested blocks).
	for _, f := range r.Fields {
		if f.Block != nil {
			continue
		}
		apply(f.Key, f.Value)
	}
	// Pass 2: nested strategy block — overrides direct values, mirroring
	// the old parser's allLines = expanded + strategyLines precedence.
	for _, f := range r.Fields {
		if f.Key != "strategy" || f.Block == nil {
			continue
		}
		for _, sf := range f.Block.Fields {
			apply(sf.Key, sf.Value)
		}
	}
	cfg.retryPolicies[r.Name] = p
}

// bamlValueScalar reconstructs the string the old parser would have
// produced from a Value after cleanBamlValue: quotes stripped from a
// Literal, env.X reassembled for an EnvRef so downstream consumers see
// the same "env.X" lexeme the line-based parser produced.
func bamlValueScalar(v *bamlparser.Value) string {
	if v == nil {
		return ""
	}
	switch {
	case v.IsLiteral():
		s, _ := v.LiteralValue()
		return s
	case v.IsIdent():
		s, _ := v.IdentValue()
		return s
	case v.IsNumber():
		s, _ := v.NumberValue()
		return s
	case v.IsRaw():
		s, _ := v.RawValue()
		return s
	case v.IsEnvRef():
		name, _ := v.EnvName()
		return "env." + name
	}
	return ""
}

// bamlValueStrategyList converts a List Value into a slice of client
// names, mirroring parseStrategyList's whitespace/comma splitting and
// quote stripping (the latter handled inside Value.String for literals).
func bamlValueStrategyList(v *bamlparser.Value) []string {
	if v == nil || v.List == nil {
		return nil
	}
	var out []string
	for _, e := range v.List {
		s, ok := e.String()
		if !ok {
			continue
		}
		s = strings.TrimSpace(s)
		if s != "" {
			out = append(out, s)
		}
	}
	return out
}

// bamlValueBedrockOption extracts a Bedrock option value with key-aware
// validation. A bare `env.IDENT` becomes Provenance=env (EnvVar=IDENT);
// anything else with a non-empty string representation becomes
// Provenance=literal (Literal=that string).
//
// For aws-bedrock clients, a declared-empty literal on one of the
// validated keys returns (zero, false, validationError):
//   - `region` — invalid per upstream BAML semantics (engine/baml-lib
//     /llm-client/src/clients/aws_bedrock.rs:256-261, "region cannot be
//     empty"); upstream errors directly.
//   - `access_key_id` / `secret_access_key` — upstream BAML accepts the
//     empty literal and signs the actual Bedrock request with literally
//     empty SigV4 credentials (Credential=/<date>/<region>/bedrock/
//     aws4_request), so AWS rejects at request time with a confusing
//     AWS-side error. Failing at parse time gives operators the
//     clearest signal ("you wrote an empty access_key_id, fix your
//     .baml") rather than letting the silent skip fall through to the
//     default credential chain and accidentally "work" with ambient
//     env creds. See #268 for the per-key actual-BAML-behaviour
//     verification.
//
// Other keys (`session_token`, `profile`, `endpoint_url`) continue to
// return (zero, false, nil) on empty literals — equivalent to upstream
// BAML's effective behaviour (session_token/endpoint_url collapse to
// None; profile routes to the default credential chain), and preserved
// for non-Bedrock clients that happen to use the same option keys.
//
// The error is returned as *bamlValidationError (nil-means-no-error) to
// keep the helper's hot path branch-light.
func bamlValueBedrockOption(clientName, key string, isBedrockClient bool, v *bamlparser.Value) (bedrockOptionValue, bool, *bamlValidationError) {
	if v == nil {
		return bedrockOptionValue{}, false, nil
	}
	if name, ok := v.EnvName(); ok {
		if name == "" {
			return bedrockOptionValue{}, false, nil
		}
		return bedrockOptionValue{EnvVar: name, Provenance: "env"}, true, nil
	}
	s, ok := v.String()
	if !ok {
		return bedrockOptionValue{}, false, nil
	}
	if s == "" {
		if isBedrockClient {
			switch key {
			case "region":
				return bedrockOptionValue{}, false, &bamlValidationError{
					Client: clientName,
					Key:    key,
					Reason: "region cannot be empty",
				}
			case "access_key_id":
				return bedrockOptionValue{}, false, &bamlValidationError{
					Client: clientName,
					Key:    key,
					Reason: "access_key_id cannot be empty",
				}
			case "secret_access_key":
				return bedrockOptionValue{}, false, &bamlValidationError{
					Client: clientName,
					Key:    key,
					Reason: "secret_access_key cannot be empty",
				}
			}
		}
		return bedrockOptionValue{}, false, nil
	}
	return bedrockOptionValue{Literal: s, Provenance: "literal"}, true, nil
}

// generateBamlConfigVars parses .baml source files and generates introspected
// variables for provider detection and retry policy configuration.
// emitFallbackRoundRobinDeferredWarnings logs a build-time warning per
// (fallback parent, deferred-shape child) pair for compositions that
// the BuildRequest resolver does NOT centralise — runtime rotation
// still works, but it routes through BAML's per-worker runtime rather
// than the cross-worker SharedState advancer. The static
// `fallback[rr[A,B], C]` shape — RR child whose every leaf is a
// non-strategy client — is centralised and does NOT warn.
//
// Deferred shapes statically introspectable from the chain map alone:
//
//  1. Immediate RR fallback child whose chain contains at least one
//     strategy leaf (e.g. `fallback[rr[fallback[A,B], C], D]`).
//     Recursive strategy planning inside a fallback child isn't
//     modelled, so the RR child stays on the legacy callback path.
//
//  2. Nested fallback that reaches an RR descendant at any depth
//     (e.g. `fallback[fallback[rr[A,B], C], D]` AND
//     `fallback[fallback[fallback[rr[A,B], C], D], E]`).
//     Centralisation only reaches IMMEDIATE fallback children of the
//     outer fallback; any RR descendant reached through one or more
//     nested fallback hops stays on the legacy per-worker path.
//     A DFS over `cfg.fallbackChains` (following only fallback nodes,
//     visited-set for cycle safety) finds the first reachable RR
//     descendant; the loop stops on the first match per (parent,
//     child) pair so a chain containing multiple paths to the same
//     RR client emits exactly one warning.
//
// Out-of-scope shapes not detectable statically (unsupported leaf
// providers like aws-bedrock, runtime overrides on options.strategy
// or options.start) don't warn at introspect time — the runtime
// resolver handles them via PathReasonFallbackRoundRobinChildLegacy.
//
// Keys are sorted so the warning order is stable across runs.
// `logf` is the writer abstraction (production: log.Printf;
// tests substitute a capture).
func emitFallbackRoundRobinDeferredWarnings(cfg *bamlConfig, logf func(string, ...any)) {
	parents := make([]string, 0, len(cfg.fallbackChains))
	for parent := range cfg.fallbackChains {
		parents = append(parents, parent)
	}
	sort.Strings(parents)
	isStrategyProvider := func(p string) bool {
		return p == "baml-roundrobin" || p == "baml-fallback"
	}
	for _, parent := range parents {
		if cfg.clientProvider[parent] != "baml-fallback" {
			continue
		}
		for _, child := range cfg.fallbackChains[parent] {
			childProvider := cfg.clientProvider[child]
			switch childProvider {
			case "baml-roundrobin":
				// Deferred shape 1: warn only when at least one RR
				// leaf is itself a strategy wrapper. RR with purely
				// non-strategy leaves is centralised.
				rrHasStrategyLeaf := false
				for _, leaf := range cfg.fallbackChains[child] {
					if isStrategyProvider(cfg.clientProvider[leaf]) {
						rrHasStrategyLeaf = true
						break
					}
				}
				if !rrHasStrategyLeaf {
					continue
				}
				logf("baml-rest introspect: fallback client %q has round-robin child %q "+
					"whose chain contains a strategy leaf; this composition stays on the "+
					"legacy per-worker rotation path because recursive strategy planning "+
					"inside a fallback child isn't modelled yet",
					parent, child)
			case "baml-fallback":
				// Deferred shape 2: nested fallback containing RR at
				// any depth. DFS through fallback nodes only; stop on
				// the first RR descendant per (parent, child) pair.
				// Visited-set guards against cycles in malformed
				// configs and against duplicate logs when multiple
				// paths converge on the same RR client.
				visited := make(map[string]bool)
				var findRoundRobinDescendant func(string) (string, bool)
				findRoundRobinDescendant = func(node string) (string, bool) {
					if visited[node] {
						return "", false
					}
					visited[node] = true
					if cfg.clientProvider[node] == "baml-roundrobin" {
						return node, true
					}
					if cfg.clientProvider[node] != "baml-fallback" {
						// Non-strategy leaf — no RR below this branch.
						return "", false
					}
					for _, next := range cfg.fallbackChains[node] {
						if rr, ok := findRoundRobinDescendant(next); ok {
							return rr, true
						}
					}
					return "", false
				}
				if rr, ok := findRoundRobinDescendant(child); ok {
					logf("baml-rest introspect: fallback client %q has nested fallback "+
						"child %q whose chain reaches round-robin descendant %q; this composition "+
						"stays on the legacy per-worker rotation path because centralisation "+
						"only reaches immediate fallback children",
						parent, child, rr)
				}
			}
		}
	}
}

func generateBamlConfigVars(out *jen.File, cliCfg *config) {
	retryPkg := cliCfg.retryPkg()

	// Parse .baml source files from the configured source directory.
	cfg := parseBamlSourceDir(cliCfg.BAMLSourceDir)

	// Stop generation if the walker accumulated any semantic-validation
	// errors (e.g. an aws-bedrock client with a declared-empty bedrock
	// option like `region`, `access_key_id`, or `secret_access_key`).
	// log.Fatalf prevents out.Save from running so introspected.go is
	// not written with a half-resolved config that would later either
	// fail at request-build time (region) or silently fall through to
	// the default credential chain and mask the config bug
	// (credentials). Matches the existing panic-on-Save-error posture
	// for fatal generation failures elsewhere in this file.
	if len(cfg.validationErrors) > 0 {
		msgs := make([]string, 0, len(cfg.validationErrors))
		for _, e := range cfg.validationErrors {
			msgs = append(msgs, e.Message())
		}
		log.Fatalf("introspect: %d config validation error(s):\n  %s",
			len(cfg.validationErrors), strings.Join(msgs, "\n  "))
	}

	// Build FunctionClient: function → client name
	out.Comment("FunctionClient maps BAML function names to their default client name")
	fcEntries := make([]jen.Code, 0, len(cfg.functionClient))
	fcKeys := make([]string, 0, len(cfg.functionClient))
	for k := range cfg.functionClient {
		fcKeys = append(fcKeys, k)
	}
	sort.Strings(fcKeys)
	for _, funcName := range fcKeys {
		clientName := cfg.functionClient[funcName]
		fcEntries = append(fcEntries, jen.Lit(funcName).Op(":").Lit(clientName))
	}
	out.Var().Id("FunctionClient").Op("=").Map(jen.String()).String().Values(fcEntries...)

	// Build FunctionProvider: function → provider (resolved through client)
	functionProvider := make(map[string]string)
	for funcName, clientName := range cfg.functionClient {
		if provider, ok := cfg.clientProvider[clientName]; ok {
			functionProvider[funcName] = provider
		}
	}

	out.Comment("FunctionProvider maps BAML function names to their default provider string")
	fpEntries := make([]jen.Code, 0, len(functionProvider))
	fpKeys := make([]string, 0, len(functionProvider))
	for k := range functionProvider {
		fpKeys = append(fpKeys, k)
	}
	sort.Strings(fpKeys)
	for _, funcName := range fpKeys {
		provider := functionProvider[funcName]
		fpEntries = append(fpEntries, jen.Lit(funcName).Op(":").Lit(provider))
	}
	out.Var().Id("FunctionProvider").Op("=").Map(jen.String()).String().Values(fpEntries...)

	// Build ClientProvider
	out.Comment("ClientProvider maps BAML client names to their provider strings")
	cpEntries := make([]jen.Code, 0, len(cfg.clientProvider))
	cpKeys := make([]string, 0, len(cfg.clientProvider))
	for k := range cfg.clientProvider {
		cpKeys = append(cpKeys, k)
	}
	sort.Strings(cpKeys)
	for _, clientName := range cpKeys {
		provider := cfg.clientProvider[clientName]
		cpEntries = append(cpEntries, jen.Lit(clientName).Op(":").Lit(provider))
	}
	out.Var().Id("ClientProvider").Op("=").Map(jen.String()).String().Values(cpEntries...)

	// Build RetryPolicies
	out.Comment("RetryPolicies maps retry policy names to their resolved Policy structs")
	rpEntries := make([]jen.Code, 0, len(cfg.retryPolicies))
	rpKeys := make([]string, 0, len(cfg.retryPolicies))
	for k := range cfg.retryPolicies {
		rpKeys = append(rpKeys, k)
	}
	sort.Strings(rpKeys)
	for _, policyName := range rpKeys {
		p := cfg.retryPolicies[policyName]
		strategyDict := jen.Dict{
			jen.Id("Type"):    jen.Lit(p.strategy),
			jen.Id("DelayMs"): jen.Lit(p.delayMs),
		}
		if p.strategy == "exponential_backoff" {
			strategyDict[jen.Id("Multiplier")] = jen.Lit(p.multiplier)
			strategyDict[jen.Id("MaxDelayMs")] = jen.Lit(p.maxDelayMs)
		}
		rpEntries = append(rpEntries, jen.Lit(policyName).Op(":").Op("&").Qual(retryPkg, "Policy").Values(jen.Dict{
			jen.Id("MaxRetries"):     jen.Lit(p.maxRetries),
			jen.Id("StrategyConfig"): jen.Op("&").Qual(retryPkg, "StrategyConfig").Values(strategyDict),
		}))
	}
	out.Var().Id("RetryPolicies").Op("=").Map(jen.String()).Op("*").Qual(retryPkg, "Policy").Values(rpEntries...)

	// Build FunctionRetryPolicy: function → policy name (resolved through client)
	functionRetryPolicy := make(map[string]string)
	for funcName, clientName := range cfg.functionClient {
		if policyName, ok := cfg.clientRetryPolicy[clientName]; ok {
			functionRetryPolicy[funcName] = policyName
		}
	}

	out.Comment("FunctionRetryPolicy maps BAML function names to their retry policy name")
	frpEntries := make([]jen.Code, 0, len(functionRetryPolicy))
	frpKeys := make([]string, 0, len(functionRetryPolicy))
	for k := range functionRetryPolicy {
		frpKeys = append(frpKeys, k)
	}
	sort.Strings(frpKeys)
	for _, funcName := range frpKeys {
		policyName := functionRetryPolicy[funcName]
		frpEntries = append(frpEntries, jen.Lit(funcName).Op(":").Lit(policyName))
	}
	out.Var().Id("FunctionRetryPolicy").Op("=").Map(jen.String()).String().Values(frpEntries...)

	// ClientRetryPolicy: the per-client retry policy name declared in
	// each client's options block. The BuildRequest router uses this,
	// keyed on the *effective* client (after any baml-roundrobin unwrap),
	// rather than FunctionRetryPolicy — which is fixed to the function's
	// declared default client. For a function whose default client is
	// an RR wrapper, FunctionRetryPolicy returns the wrapper's policy,
	// not the child that actually handles the request; ClientRetryPolicy
	// lets the router pick up the correct child-level policy.
	out.Comment("ClientRetryPolicy maps BAML client names to their declared retry policy name")
	{
		entries := make([]jen.Code, 0, len(cfg.clientRetryPolicy))
		keys := make([]string, 0, len(cfg.clientRetryPolicy))
		for k := range cfg.clientRetryPolicy {
			keys = append(keys, k)
		}
		sort.Strings(keys)
		for _, clientName := range keys {
			entries = append(entries, jen.Lit(clientName).Op(":").Lit(cfg.clientRetryPolicy[clientName]))
		}
		out.Var().Id("ClientRetryPolicy").Op("=").Map(jen.String()).String().Values(entries...)
	}

	// Scan for fallback compositions whose RR-child centralisation is
	// still deferred and emit a one-time build-log warning per (fallback,
	// rr-child) pair. See emitFallbackRoundRobinDeferredWarnings for the
	// deferred-shape matrix.
	emitFallbackRoundRobinDeferredWarnings(cfg, log.Printf)

	// FallbackChains: maps strategy client names (baml-fallback, baml-roundrobin)
	// to their ordered list of child client names from the strategy option.
	out.Comment("FallbackChains maps strategy client names to their ordered list of child client names")
	{
		entries := make([]jen.Code, 0, len(cfg.fallbackChains))
		keys := make([]string, 0, len(cfg.fallbackChains))
		for k := range cfg.fallbackChains {
			keys = append(keys, k)
		}
		sort.Strings(keys)
		for _, clientName := range keys {
			chain := cfg.fallbackChains[clientName]
			childLits := make([]jen.Code, 0, len(chain))
			for _, child := range chain {
				childLits = append(childLits, jen.Lit(child))
			}
			entries = append(entries, jen.Lit(clientName).Op(":").Index().String().Values(childLits...))
		}
		out.Var().Id("FallbackChains").Op("=").Map(jen.String()).Index().String().Values(entries...)
	}

	// RoundRobinStart captures the `start` option from each baml-roundrobin
	// client's options block. Only clients that explicitly set `start N`
	// appear here; others rely on the coordinator's random seed. The map
	// is passed to the coordinator constructor so static RR rotations
	// begin deterministically when the operator asks for it.
	//
	// When the .baml source defines no baml-roundrobin clients at all,
	// RoundRobinStart is emitted as a plain nil declaration rather than
	// an empty map literal. cmd/serve uses the nil-ness to decide
	// whether to stand up the host-side SharedState gRPC server — an
	// installation with no RR clients pays no setup cost for the
	// reverse-broker channel that would otherwise be unused.
	hasRoundRobinClients := false
	for _, provider := range cfg.clientProvider {
		if provider == "baml-roundrobin" {
			hasRoundRobinClients = true
			break
		}
	}
	out.Comment("RoundRobinStart maps baml-roundrobin client names to their configured start index")
	out.Comment("Nil when the source defines no baml-roundrobin clients; cmd/serve uses that as the gate")
	out.Comment("for provisioning the host-side SharedState server.")
	if !hasRoundRobinClients {
		out.Var().Id("RoundRobinStart").Map(jen.String()).Int()
	} else {
		// Filter to baml-roundrobin clients only. cfg.roundRobinStart
		// is populated by the `start N` option extractor, which the
		// parser runs on every options block — nothing in the parser
		// is RR-specific, so a non-RR client that happens to declare
		// `start N` (a provider-specific option or a typo) would end
		// up here and get fed to the Coordinator's per-client seed
		// map as if it were an RR rotation seed. Dropping non-RR
		// entries at emission time keeps the generated map honest.
		entries := make([]jen.Code, 0, len(cfg.roundRobinStart))
		keys := make([]string, 0, len(cfg.roundRobinStart))
		for k := range cfg.roundRobinStart {
			if cfg.clientProvider[k] != "baml-roundrobin" {
				continue
			}
			keys = append(keys, k)
		}
		sort.Strings(keys)
		for _, clientName := range keys {
			entries = append(entries, jen.Lit(clientName).Op(":").Lit(cfg.roundRobinStart[clientName]))
		}
		out.Var().Id("RoundRobinStart").Op("=").Map(jen.String()).Int().Values(entries...)
	}

	// RoundRobinCoordinator: one persistent coordinator per generated
	// adapter package. The BuildRequest path routes static round-robin
	// selection through it so counters live for the process lifetime
	// instead of resetting per request. Dynamic RR clients (registered
	// purely through client_registry overrides) bypass this coordinator.
	out.Comment("RoundRobinCoordinator holds per-client round-robin counters shared across requests")
	out.Var().Id("RoundRobinCoordinator").Op("=").Qual(cliCfg.roundRobinPkg(), "NewCoordinatorWithStarts").Call(jen.Id("RoundRobinStart"))

	// BedrockOptionValue + BedrockClientOptions carry the parsed
	// aws-bedrock client options (endpoint_url, region, and the
	// credential selectors access_key_id / secret_access_key /
	// session_token / profile) that codegen reads at request-build
	// time to override BAML v0.219's hardcoded modular Bedrock URL,
	// pin SigV4 region, and select per-client credentials. Resolution
	// happens at runtime so env-var values reflect the worker's
	// current environment rather than the build-time snapshot.
	emitBedrockOptionTypes(out)

	// BedrockClientOptionsByName: keyed by client name; only aws-bedrock
	// clients are emitted. The codegen dispatcher in
	// adapters/common/codegen treats absence-from-map as "no override
	// configured" and falls through to MaybeAttachBedrockAuth's
	// URL-pattern detection for the default-endpoint case.
	//
	// Variable name disambiguates from the struct type
	// `BedrockClientOptions`: Go's package namespace would otherwise
	// reject `var BedrockClientOptions = map[string]BedrockClientOptions{}`.
	out.Comment("BedrockClientOptionsByName maps aws-bedrock client names to their parsed endpoint_url, region,")
	out.Comment("and credential-selector options. Codegen looks up the resolved client name here at request-build")
	out.Comment("time and dispatches to llmhttp.AttachBedrockAuthForClient with the resolved values.")
	{
		entries := make([]jen.Code, 0, len(cfg.bedrockClientOptions))
		keys := make([]string, 0, len(cfg.bedrockClientOptions))
		for k := range cfg.bedrockClientOptions {
			// Provider filter: a non-bedrock client that happens to
			// declare endpoint_url / region / credential keys (a typo,
			// a future provider sharing a key name) must NOT leak into
			// the runtime map — codegen looks up by client name and
			// would wire AttachBedrockAuthForClient against a
			// non-Bedrock URL, silently changing behaviour (and worse,
			// pointing static AWS credentials at the wrong endpoint).
			// Filtering here keeps the generated map honest.
			if cfg.clientProvider[k] != "aws-bedrock" {
				continue
			}
			keys = append(keys, k)
		}
		sort.Strings(keys)
		for _, clientName := range keys {
			opts := cfg.bedrockClientOptions[clientName]
			entries = append(entries, jen.Lit(clientName).Op(":").Id("BedrockClientOptions").Values(jen.Dict{
				jen.Id("EndpointURL"): bedrockOptionValueLit(opts.EndpointURL),
				jen.Id("Region"):      bedrockOptionValueLit(opts.Region),
				jen.Id("Credentials"): jen.Id("BedrockCredentialOptions").Values(jen.Dict{
					jen.Id("AccessKeyID"):     bedrockOptionValueLit(opts.Credentials.AccessKeyID),
					jen.Id("SecretAccessKey"): bedrockOptionValueLit(opts.Credentials.SecretAccessKey),
					jen.Id("SessionToken"):    bedrockOptionValueLit(opts.Credentials.SessionToken),
					jen.Id("Profile"):         bedrockOptionValueLit(opts.Credentials.Profile),
				}),
			}))
		}
		out.Var().Id("BedrockClientOptionsByName").Op("=").Map(jen.String()).Id("BedrockClientOptions").Values(entries...)
	}
}

// bedrockOptionValueLit renders a bedrockOptionValue as the
// generated-Go literal for a BedrockOptionValue struct. Empty
// (Provenance == "") values still render explicitly so the diff between
// "EndpointURL set, Region unset" and "both set" is visually obvious in
// the generated source.
func bedrockOptionValueLit(v bedrockOptionValue) jen.Code {
	return jen.Id("BedrockOptionValue").Values(jen.Dict{
		jen.Id("Literal"):    jen.Lit(v.Literal),
		jen.Id("EnvVar"):     jen.Lit(v.EnvVar),
		jen.Id("Provenance"): jen.Lit(v.Provenance),
	})
}

// emitBedrockOptionTypes emits the BedrockOptionValue struct, its
// Resolve() method, and the BedrockClientOptions struct into the
// generated introspected package. These types are part of the package's
// stable surface so the codegen-emitted dispatch and the
// runtime-emitted resolver agree on shape.
//
// Resolve() returns:
//   - (Literal, true)            when Provenance == "literal"
//   - (os.LookupEnv(EnvVar))     when Provenance == "env"
//   - ("", false)                when Provenance == ""
//
// The runtime branch resolves env vars per-call so a worker that
// reloads its environment between requests sees the new value.
func emitBedrockOptionTypes(out *jen.File) {
	out.Comment("BedrockOptionValue carries one parsed aws-bedrock option value with its provenance preserved.")
	out.Comment("Provenance discriminates the value source: \"literal\" (Literal carries the bare string),")
	out.Comment("\"env\" (EnvVar carries the env var name), or \"\" (no value declared).")
	out.Type().Id("BedrockOptionValue").Struct(
		jen.Id("Literal").String(),
		jen.Id("EnvVar").String(),
		jen.Id("Provenance").String(),
	)

	out.Comment("Resolve returns (value, ok) for this option:")
	out.Comment("  - literal provenance: (Literal, true)")
	out.Comment("  - env provenance:     os.LookupEnv(EnvVar)")
	out.Comment("  - unset:              (\"\", false)")
	out.Func().Params(jen.Id("v").Id("BedrockOptionValue")).Id("Resolve").Params().Params(jen.String(), jen.Bool()).Block(
		jen.Switch(jen.Id("v").Dot("Provenance")).Block(
			jen.Case(jen.Lit("literal")).Block(
				jen.Return(jen.Id("v").Dot("Literal"), jen.True()),
			),
			jen.Case(jen.Lit("env")).Block(
				jen.Return(jen.Qual("os", "LookupEnv").Call(jen.Id("v").Dot("EnvVar"))),
			),
		),
		jen.Return(jen.Lit(""), jen.False()),
	)

	out.Comment("IsSet reports whether the field was declared in the .baml options block,")
	out.Comment("irrespective of whether its value resolves to a non-empty string at runtime.")
	out.Comment("Codegen uses IsSet to populate BedrockCredentialSelector presence flags so")
	out.Comment("declared-but-env-unset credentials are still routed through the resolver's")
	out.Comment("static-branch validation (where they fail with a client-scoped error) rather")
	out.Comment("than silently falling through to the default AWS credential chain.")
	out.Comment("Use Resolve for the value; IsSet for presence.")
	out.Func().Params(jen.Id("v").Id("BedrockOptionValue")).Id("IsSet").Params().Bool().Block(
		jen.Return(jen.Id("v").Dot("Provenance").Op("!=").Lit("")),
	)

	out.Comment("BedrockCredentialOptions groups the per-client aws-bedrock credential selectors parsed from .baml")
	out.Comment("options (#254 item 2). Resolution precedence (applied by bamlutils/llmhttp.ResolveBedrockCredentials):")
	out.Comment("  1. AccessKeyID + SecretAccessKey (+ optional SessionToken) -> static credentials.")
	out.Comment("  2. Profile -> shared-config profile load.")
	out.Comment("  3. Default chain (none of the above declared).")
	out.Comment("")
	out.Comment("SECURITY: literal credential values declared inline land in this generated file AND the compiled")
	out.Comment("worker binary. That is BAML-parity behavior, documented as an operator footgun. Prefer env.X")
	out.Comment("references for any secret material so values stay out of generated artifacts.")
	out.Type().Id("BedrockCredentialOptions").Struct(
		jen.Id("AccessKeyID").Id("BedrockOptionValue"),
		jen.Id("SecretAccessKey").Id("BedrockOptionValue"),
		jen.Id("SessionToken").Id("BedrockOptionValue"),
		jen.Id("Profile").Id("BedrockOptionValue"),
	)

	out.Comment("BedrockClientOptions carries the per-client aws-bedrock options that codegen needs at request-build time")
	out.Comment("to override BAML's hardcoded modular Bedrock URL (BAML v0.219 hardcodes")
	out.Comment("https://bedrock-runtime.<region>.amazonaws.com and the public Go HTTPRequest surface does not expose")
	out.Comment("ClientDetails, so baml-rest carries its own resolved options keyed on client name).")
	out.Type().Id("BedrockClientOptions").Struct(
		jen.Id("EndpointURL").Id("BedrockOptionValue"),
		jen.Id("Region").Id("BedrockOptionValue"),
		jen.Id("Credentials").Id("BedrockCredentialOptions"),
	)
}
