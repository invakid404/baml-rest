func extractTypeBuilderMethods(f *parsedFile) []TypeBuilderMethod {
	var methods []TypeBuilderMethod
	for _, decl := range f.file.Decls {
		funcDecl, ok := decl.(*ast.FuncDecl)
		if !ok || funcDecl.Recv == nil || len(funcDecl.Recv.List) == 0 {
			continue
		}
		receiver, ok := funcDecl.Recv.List[0].Type.(*ast.StarExpr)
		if !ok {
			continue
		}
		receiverIdent, ok := receiver.X.(*ast.Ident)
		if !ok || receiverIdent.Name != "TypeBuilder" {
			continue
		}
		if funcDecl.Type.Results == nil || len(funcDecl.Type.Results.List) != 2 {
			continue
		}
		firstResult, ok := funcDecl.Type.Results.List[0].Type.(*ast.StarExpr)
		if !ok {
			continue
		}
		returnTypeIdent, ok := firstResult.X.(*ast.Ident)
		if !ok {
			continue
		}
		returnTypeName := returnTypeIdent.Name

		var isClass, isDynamic bool
		var typeName string

		if strings.HasSuffix(returnTypeName, "ClassBuilder") {
			isClass, isDynamic = true, true
			typeName = strings.TrimSuffix(returnTypeName, "ClassBuilder")
		} else if strings.HasSuffix(returnTypeName, "ClassView") {
			isClass, isDynamic = true, false
			typeName = strings.TrimSuffix(returnTypeName, "ClassView")
		} else if strings.HasSuffix(returnTypeName, "EnumBuilder") {
			isClass, isDynamic = false, true
			typeName = strings.TrimSuffix(returnTypeName, "EnumBuilder")
		} else if strings.HasSuffix(returnTypeName, "EnumView") {
			isClass, isDynamic = false, false
			typeName = strings.TrimSuffix(returnTypeName, "EnumView")
		} else {
			continue
		}

		methods = append(methods, TypeBuilderMethod{
			Name:       funcDecl.Name.Name,
			TypeName:   typeName,
			ReturnType: returnTypeName,
			IsDynamic:  isDynamic,
			IsClass:    isClass,
		})
	}
	return methods
}

// generateMediaParams generates the MediaParams map variable in the introspected output.
func generateMediaParams(out *jen.File, mediaParams map[string][]mediaParamInfo, interfacesPkg string) {
	out.Comment("MediaParams maps function name -> param name -> media kind for parameters that are BAML media types.")

	if len(mediaParams) == 0 {
		out.Var().Id("MediaParams").Op("=").
			Map(jen.String()).Map(jen.String()).Qual(interfacesPkg, "MediaKind").Values()
		return
	}

	var outerEntries []jen.Code
	for funcName, params := range mediaParams {
		var innerEntries []jen.Code
		for _, p := range params {
			innerEntries = append(innerEntries,
				jen.Lit(p.paramName).Op(":").Qual(interfacesPkg, p.kindConst),
			)
		}
		outerEntries = append(outerEntries,
			jen.Lit(funcName).Op(":").Map(jen.String()).Qual(interfacesPkg, "MediaKind").Values(innerEntries...),
		)
	}

	out.Var().Id("MediaParams").Op("=").
		Map(jen.String()).Map(jen.String()).Qual(interfacesPkg, "MediaKind").Values(outerEntries...)
}

// mediaTypeNames maps BAML media type names (as they appear in Go) to their MediaKind constant names.
var mediaTypeNames = map[string]string{
	"Image": "MediaKindImage",
	"Audio": "MediaKindAudio",
	"PDF":   "MediaKindPDF",
	"Video": "MediaKindVideo",
}

// resolveBamlPkgAliases finds all import aliases for BAML-related packages in a file.
// This covers both the configured BAML runtime module (for the server build that's
// github.com/boundaryml/baml; consumers of a forked module pass the forked path) and
// the generated client root (containing the media-type constructors), since different
// BAML versions may place Image/Audio/PDF/Video types in different packages. The
// upstream github.com/boundaryml/baml prefix is always recognised so server builds
// keep working when a non-default --baml-module-path is supplied without dropping
// the canonical BAML import recognition.
// Returns a set of alias identifiers (e.g., {"baml": true, "types": true}).
func resolveBamlPkgAliases(file *ast.File, bamlModulePath, generatedClientPkg string) map[string]bool {
	aliases := make(map[string]bool)
	for _, imp := range file.Imports {
		path, err := strconv.Unquote(imp.Path.Value)
		if err != nil {
			continue
		}
		// Match any BAML-related import: the configured runtime module,
		// upstream BAML (so server builds keep working when callers pass
		// only --baml-module-path), the configured generated client root,
		// or any path containing /baml_client (the conventional generator
		// output leaf).
		if !strings.HasPrefix(path, bamlModulePath) &&
			!strings.HasPrefix(path, "github.com/boundaryml/baml") &&
			!strings.HasPrefix(path, generatedClientPkg) &&
			!strings.Contains(path, "/baml_client") {
			continue
		}
		var alias string
		if imp.Name != nil {
			alias = imp.Name.Name
		} else {
			// Default alias is the last path segment
			parts := strings.Split(path, "/")
			alias = parts[len(parts)-1]
		}
		if alias != "_" && alias != "." {
			aliases[alias] = true
		}
	}
	return aliases
}

// mediaParamInfo holds media type info for a single function parameter.
type mediaParamInfo struct {
	paramName string
	kindConst string // e.g., "MediaKindImage"
}

// extractMediaParams detects media-typed parameters in sync functions.
// Returns a map of function name -> list of media param info.
func extractMediaParams(f *parsedFile, cfg *config) map[string][]mediaParamInfo {
	bamlAliases := resolveBamlPkgAliases(f.file, cfg.BAMLModulePath, cfg.streamPkg())
	if len(bamlAliases) == 0 {
		return nil // No BAML imports, no media types possible
	}

	result := make(map[string][]mediaParamInfo)

	for _, decl := range f.file.Decls {
		funcDecl, ok := decl.(*ast.FuncDecl)
		if !ok || funcDecl.Recv != nil {
			continue
		}
		if funcDecl.Type.Params == nil || len(funcDecl.Type.Params.List) == 0 {
			continue
		}

		// Verify it's a sync function (first param is context.Context)
		firstParam := funcDecl.Type.Params.List[0]
		selectorExpr, ok := firstParam.Type.(*ast.SelectorExpr)
		if !ok {
			continue
		}
		ident, ok := selectorExpr.X.(*ast.Ident)
		if !ok || ident.Name != "context" || selectorExpr.Sel.Name != "Context" {
			continue
		}

		var mediaParams []mediaParamInfo

		// Check each parameter (skip first=context, skip last=variadic opts)
		params := funcDecl.Type.Params.List
		for _, param := range params[1:] {
			kindConst := resolveMediaType(param.Type, bamlAliases)
			if kindConst == "" {
				continue
			}
			for _, name := range param.Names {
				mediaParams = append(mediaParams, mediaParamInfo{
					paramName: name.Name,
					kindConst: kindConst,
				})
			}
		}

		if len(mediaParams) > 0 {
			result[funcDecl.Name.Name] = mediaParams
		}
	}

	if len(result) == 0 {
		return nil
	}
	return result
}

// resolveMediaType checks if a type expression is a BAML media type.
// Handles direct types (pkg.Image), pointers (*pkg.Image), and slices ([]pkg.Image).
// Checks against all known BAML package aliases.
// Returns the MediaKind constant name or empty string if not a media type.
func resolveMediaType(expr ast.Expr, bamlAliases map[string]bool) string {
	switch t := expr.(type) {
	case *ast.SelectorExpr:
		// Direct type: pkg.Image
		if ident, ok := t.X.(*ast.Ident); ok && bamlAliases[ident.Name] {
			if kindConst, ok := mediaTypeNames[t.Sel.Name]; ok {
				return kindConst
			}
		}
	case *ast.StarExpr:
		// Pointer: *pkg.Image (optional media)
		return resolveMediaType(t.X, bamlAliases)
	case *ast.ArrayType:
		// Slice: []pkg.Image (list of media)
		if t.Len == nil { // slice, not fixed-size array
			return resolveMediaType(t.Elt, bamlAliases)
		}
	}
	return ""
}

