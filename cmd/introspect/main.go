// config carries the per-invocation paths and import roots that
// generateStub / generateFull thread through every emitter. The
// defaults reproduce the server build's root layout
// (baml_client/ + introspected/ rooted at the repo module path);
// callers wishing to drive introspection against a different layout
// (e.g. a vendored generated tree under another package) override the
// relevant fields via CLI flags before passing the struct down.
type config struct {
	Stub           bool
	InputDir       string
	BAMLSourceDir  string
	OutputDir      string
	PackageName    string
	ModulePath     string
	ClientPkgName  string
	InterfacesPkg  string
	BAMLModulePath string
}

// streamPkg returns the generated-client root import path used to
// reference Stream / Parse / ParseStream / Request / StreamRequest /
// sync functions from the emitted introspected.go.
func (c *config) streamPkg() string {
	return c.ModulePath + "/" + c.ClientPkgName
}

// typeBuilderPkg returns the generated-client type_builder subpackage
// import path used by the TypeBuilder type aliases.
func (c *config) typeBuilderPkg() string {
	return c.streamPkg() + "/type_builder"
}

// retryPkg returns the bamlutils retry subpackage import path used
// by RetryPolicies in the emitted output and forced-imported into the
// stub so the file compiles even when the maps are empty.
func (c *config) retryPkg() string {
	return c.InterfacesPkg + "/retry"
}

// roundRobinPkg returns the bamlutils round-robin coordinator
// subpackage import path used by RoundRobinCoordinator in the emitted
// output.
func (c *config) roundRobinPkg() string {
	return c.InterfacesPkg + "/buildrequest/roundrobin"
}

// outputFile returns the path of the introspected.go file to emit.
func (c *config) outputFile() string {
	return filepath.Join(c.OutputDir, "introspected.go")
}

type parsedFile struct {
	file *ast.File
	path string
}

// TypeBuilderMethod represents a method on TypeBuilder that returns a class/enum accessor
type TypeBuilderMethod struct {
	Name       string // Method name on TypeBuilder (e.g., "Resume", "DynamicTestOutput")
	TypeName   string // The BAML type name (same as method name)
	ReturnType string // The return type name (e.g., "ResumeClassView", "DynamicTestOutputClassBuilder")
	IsDynamic  bool   // true if returns Builder, false if returns View
	IsClass    bool   // true for class, false for enum
}

func main() {
	cfg := &config{
		InputDir:       "baml_client",
		BAMLSourceDir:  "baml_src",
		OutputDir:      "introspected",
		PackageName:    "introspected",
		ModulePath:     "github.com/invakid404/baml-rest",
		InterfacesPkg:  "github.com/invakid404/baml-rest/bamlutils",
		BAMLModulePath: "github.com/boundaryml/baml",
	}

	flag.BoolVar(&cfg.Stub, "stub", false, "Generate stub file (no baml_client required)")
	flag.StringVar(&cfg.InputDir, "input-dir", cfg.InputDir, "Path to the generated BAML Go client directory")
	flag.StringVar(&cfg.BAMLSourceDir, "baml-src-dir", cfg.BAMLSourceDir, "Path to the .baml source directory")
	flag.StringVar(&cfg.OutputDir, "output-dir", cfg.OutputDir, "Output directory for the generated introspected package")
	flag.StringVar(&cfg.PackageName, "package", cfg.PackageName, "Package name for the generated file")
	flag.StringVar(&cfg.ModulePath, "module-path", cfg.ModulePath, "Module path that hosts the generated client and introspected packages")
	clientPkgFlag := flag.String("client-pkg", "", "Generated client package leaf name (defaults to filepath.Base of --input-dir)")
	flag.StringVar(&cfg.InterfacesPkg, "interfaces-pkg", cfg.InterfacesPkg, "Import path of the bamlutils interfaces package")
	flag.StringVar(&cfg.BAMLModulePath, "baml-module-path", cfg.BAMLModulePath, "Import path of the BAML Go runtime module")
	flag.Parse()

	if *clientPkgFlag != "" {
		cfg.ClientPkgName = *clientPkgFlag
	} else {
		cfg.ClientPkgName = filepath.Base(cfg.InputDir)
	}

	if cfg.Stub {
		generateStub(cfg)
		return
	}

	generateFull(cfg)
}

